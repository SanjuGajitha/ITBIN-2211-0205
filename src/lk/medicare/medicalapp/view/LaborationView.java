    private void deletebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deletebtnMouseClicked
        // TODO add your handling code here:
int selectedRow = laborlist.getSelectedRow();
        if (selectedRow >= 0) {
            
        int id = Integer.parseInt(laborlist.getValueAt(selectedRow, 0).toString());

     //   System.out.print(id);
        
        if (JOptionPane.showConfirmDialog(this, "Are you sure you want to delete?", "Delete Confirmation", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
           
                // Send delete request to Controller (assuming communication mechanism)
                // System.out.println(id);
                LaborationController controller = new LaborationController(this); // Assuming an instance creation mechanism
                controller.deletedlabor(id);
                
                formWindowOpened(null);
                
                 JOptionPane.showMessageDialog(new JFrame(),"DATA Deleted Successfully ");
           
            
        }
    } 
        
        
    }//GEN-LAST:event_deletebtnMouseClicked

