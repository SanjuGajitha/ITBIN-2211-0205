    private void updatebtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_updatebtnMouseClicked
        int id = Integer.parseInt(laborlist.getValueAt(laborlist.getSelectedRow(), 0).toString());
  
        
        Laboration labor = new Laboration();
        
          labor.setSpecimenType((String) specimen.getSelectedItem());
          labor.setHealthScreening((String) health.getSelectedItem());
          labor.setTestAtHome((String) home.getSelectedItem());
           labor.setLaborId(id);
      
        
        LaborationController controller = new LaborationController(this); // Assuming an instance creation mechanism

        controller.updatelabor(labor);
        formWindowOpened(null); 
        
             JOptionPane.showMessageDialog(new JFrame(),"DATA Updated Successfully ");
        
       
    }//GEN-LAST:event_updatebtnMouseClicked

